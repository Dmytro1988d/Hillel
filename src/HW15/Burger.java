package HW15;

public class Burger {
    public String bun;
    public String meat;
    public String cheese;
    public String herbs;
    public String mayo;

    public Burger(String bun, String meat, String cheese, String herbs, String mayo) {
        this.bun = bun;
        this.meat = meat;
        this.cheese = cheese;
        this.herbs = herbs;
        this.mayo = mayo;
    }
}
